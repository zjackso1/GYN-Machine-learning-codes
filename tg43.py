# Reference point (1 cm, 90 degrees)
REF_R_CM = 1.0
REF_THETA_RAD = np.pi / 2
# g_L(r) is normalized to g_L(1 cm) = 1.0 per TG-43U1.
RADIAL_G_REF = float(np.exp(np.interp(REF_R_CM, RADIAL_DATA_CM, np.log(RADIAL_G))))


def radial_g(r_cm: float) -> float:
    # Log-linear interpolation per TG-43 guidance.
    # Normalize to g_L(1 cm) = 1.0 per TG-43U1.
    raw = float(np.exp(np.interp(float(r_cm), RADIAL_DATA_CM, np.log(RADIAL_G))))
    return float(raw / RADIAL_G_REF)


def geometry_factor_tg43(r_cm, theta_rad, L_cm: float = SOURCE_LENGTH_CM):
    r = np.asarray(r_cm, dtype=np.float64)
    th = np.asarray(theta_rad, dtype=np.float64)

    out = np.empty_like(r, dtype=np.float64)

    axial = np.abs(np.sin(th)) < 1e-10
    off = ~axial

    if np.any(axial):
        denom = r[axial] ** 2 - (L_cm ** 2) / 4.0
        denom = np.maximum(denom, 1e-12)
        out[axial] = 1.0 / denom

    if np.any(off):
        num1 = r[off] * np.cos(th[off]) - L_cm / 2.0
        den1 = np.sqrt(r[off] ** 2 + (L_cm / 2.0) ** 2 - L_cm * r[off] * np.cos(th[off]))

        num2 = r[off] * np.cos(th[off]) + L_cm / 2.0
        den2 = np.sqrt(r[off] ** 2 + (L_cm / 2.0) ** 2 + L_cm * r[off] * np.cos(th[off]))

        term1 = np.arccos(np.clip(num1 / np.maximum(den1, 1e-12), -1.0, 1.0))
        term2 = np.arccos(np.clip(num2 / np.maximum(den2, 1e-12), -1.0, 1.0))

        out[off] = (term1 - term2) / (L_cm * r[off] * np.sin(th[off]))

    return out


def geometry_factor_line(rho_cm: float, z_cm: float) -> float:
    # Convert rho/z to (r, theta) and use TG-43U1 geometry factor.
    rho = float(abs(rho_cm))
    r_cm = float(np.sqrt(rho * rho + z_cm * z_cm))
    if r_cm <= 1e-12:
        return 0.0
    cos_theta = float(np.clip(z_cm / max(r_cm, 1e-12), -1.0, 1.0))
    theta_rad = float(np.arccos(cos_theta))
    return float(geometry_factor_tg43(r_cm, theta_rad, SOURCE_LENGTH_CM))


REF_GEOM = float(geometry_factor_tg43(REF_R_CM, REF_THETA_RAD, SOURCE_LENGTH_CM))
REF_RADIAL = 1.0


def radial_g_vector(r_cm_array):
    # Log-linear interpolation per TG-43 guidance, normalized to g_L(1 cm)=1.0.
    vals = np.asarray(r_cm_array, dtype=np.float32)
    raw = np.exp(np.interp(vals, RADIAL_DATA_CM, np.log(RADIAL_G))).astype(np.float32)
    return (raw / float(RADIAL_G_REF)).astype(np.float32)


def geometry_factor_line_vector(rho_cm, z_cm):
    # Convert rho/z to (r, theta) and use TG-43U1 geometry factor.
    rho = np.abs(np.asarray(rho_cm, dtype=np.float64))
    z_cm = np.asarray(z_cm, dtype=np.float64)
    r_cm = np.sqrt(rho * rho + z_cm * z_cm)
    cos_theta = np.clip(z_cm / np.maximum(r_cm, 1e-12), -1.0, 1.0)
    theta_rad = np.arccos(cos_theta)
    geom = geometry_factor_tg43(r_cm, theta_rad, SOURCE_LENGTH_CM)
    return geom.astype(np.float32)


def anisotropy_factor(r_cm, theta_rad):
    r_cm = np.asarray(r_cm, dtype=np.float32)
    theta_deg = np.degrees(theta_rad).astype(np.float32)
    # Normalize to [0, 180] (fold angles like 270 -> 90)
    theta_deg = np.mod(theta_deg, 360.0)
    theta_deg = np.where(theta_deg > 180.0, 360.0 - theta_deg, theta_deg)
    theta_deg = np.clip(theta_deg, ANISO_THETA_DEG[0], ANISO_THETA_DEG[-1])
    r_clamped = np.clip(r_cm, ANISO_RADIAL_CM[0], ANISO_RADIAL_CM[-1])

    # Bilinear interpolation in (r, theta)
    r_idx = np.searchsorted(ANISO_RADIAL_CM, r_clamped, side="right") - 1
    t_idx = np.searchsorted(ANISO_THETA_DEG, theta_deg, side="right") - 1
    r_idx = np.clip(r_idx, 0, ANISO_RADIAL_CM.size - 2)
    t_idx = np.clip(t_idx, 0, ANISO_THETA_DEG.size - 2)

    r0 = ANISO_RADIAL_CM[r_idx]
    r1 = ANISO_RADIAL_CM[r_idx + 1]
    t0 = ANISO_THETA_DEG[t_idx]
    t1 = ANISO_THETA_DEG[t_idx + 1]
    tr = np.where(r1 > r0, (r_clamped - r0) / (r1 - r0), 0.0)
    tt = np.where(t1 > t0, (theta_deg - t0) / (t1 - t0), 0.0)

    f11 = ANISO_VALUES[t_idx, r_idx]
    f21 = ANISO_VALUES[t_idx, r_idx + 1]
    f12 = ANISO_VALUES[t_idx + 1, r_idx]
    f22 = ANISO_VALUES[t_idx + 1, r_idx + 1]

    results = (1.0 - tr) * (1.0 - tt) * f11 + tr * (1.0 - tt) * f21 + (1.0 - tr) * tt * f12 + tr * tt * f22
    return results.astype(np.float32)


def dose_rate_per_unit_strength(r_cm, rho_cm, z_cm, theta_rad):
    r_arr = np.asarray(r_cm, dtype=np.float32)
    rho_arr = np.asarray(rho_cm, dtype=np.float32)
    z_arr = np.asarray(z_cm, dtype=np.float32)
    theta_arr = np.asarray(theta_rad, dtype=np.float32)

    r_arr = np.maximum(r_arr, 0.05)
    geom = geometry_factor_line_vector(rho_arr, z_arr)
    radial = radial_g_vector(r_arr)
    anis = anisotropy_factor(r_arr, theta_arr)

    denom = (REF_GEOM * REF_RADIAL)
    dose_rate = DOSE_RATE_CONSTANT * (geom * radial * anis) / denom
    return dose_rate
